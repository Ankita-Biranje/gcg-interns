// What can be done with JavaScript?

// JavaScript is a versatile programming language that can be used for a wide variety of applications.

// 1. Web Development (Frontend)
// This is the most common use of JavaScript. It allows you to create dynamic and interactive web pages.
// - Manipulate the DOM (Document Object Model) to change the content and appearance of a web page.
// - Handle user events like clicks, mouse movements, and keyboard input.
// - Make asynchronous requests to a server (AJAX/Fetch) to get or send data without reloading the page.
// - Create animations and special effects.
// - Build complex single-page applications (SPAs) with frameworks like React, Angular, or Vue.

// 2. Web Development (Backend)
// With Node.js, you can use JavaScript to build server-side applications.
// - Create web servers and APIs.
// - Interact with databases like MongoDB, MySQL, or PostgreSQL.
// - Build real-time applications like chat apps with WebSockets.
// - Create command-line tools.

// 3. Mobile App Development
// You can use JavaScript to build mobile apps for both iOS and Android.
// - React Native allows you to build native mobile apps with JavaScript and React.
// - NativeScript is another framework for building native mobile apps with JavaScript.
// - Progressive Web Apps (PWAs) are web applications that can be "installed" on a mobile device and offer an app-like experience.

// 4. Desktop App Development
// You can also use JavaScript to build desktop applications.
// - Electron is a framework that allows you to build cross-platform desktop apps with web technologies (HTML, CSS, and JavaScript).
//   Popular apps like Visual Studio Code, Slack, and Discord are built with Electron.

// 5. Game Development
// JavaScript can be used to create games that run in the browser.
// - The HTML5 <canvas> element provides a way to draw graphics and create animations.
// - Libraries like Phaser and Three.js make it easier to build 2D and 3D games.

// 6. Machine Learning
// With libraries like TensorFlow.js, you can run machine learning models in the browser or in Node.js.
// - This allows you to do things like image recognition, natural language processing, and more, right in your JavaScript application.
